import pycalc

tests = (
    ("2+2 *2", "", 6),
    ("1+2*3**4", "", 163),
    ("-1", "", -1),
    ("-2**2", "", -4),
    ("(2+3)*4", "", 20),
    ("-(1)", "", -1),
    (".3pi", "", 0.9424777960769379),
    ("pi*(-1)", "", -3.141592653589793),
    ("-pi", "", -3.141592653589793),
    ("1*4+3.3/(3 + .3)*3(sqrt(4))/(sin(0) + 1)", "", 10.0),
    ("2^(2^2)", "", 16),
    ("sin(1,)", "", 0.8414709848078965),
    # ("--1", "", 1),
    # ("3--+---++1", "", 2),
    ("maxunicode", ("sys",), 1114111),
    ("min(range(10))", "", 0),
    ("10 == 10.0", "", True),
    ("10 != 10.0", "", False),
    # ("False + 1", "", 1),
    # ("1, +1", "", (1, 1)),
    ("len(range(10)*2)", "", 20),
)

for expr, modules, result in tests:
    if pycalc.calc(expr, modules) == result:
        print("PASS\t", expr, "\t=\t", result)
    else:
        print("FAIL\t", expr, "\t!=\t", result)